# Exemplo de operadores em Python

# Operadores aritméticos
a = 10
b = 3
print("Soma:", a + b)
print("Subtração:", a - b)
print("Multiplicação:", a * b)
print("Divisão:", a / b)
print("Resto da divisão:", a % b)
print("Potência:", a ** b)

# Operadores de comparação
x = 5
y = 8
print("x é igual a y?", x == y)
print("x é diferente de y?", x != y)
print("x é maior que y?", x > y)
print("x é menor ou igual a y?", x <= y)

# Operadores lógicos
p = True
q = False
print("p e q:", p and q)
print("p ou q:", p or q)
print("não p:", not p)
