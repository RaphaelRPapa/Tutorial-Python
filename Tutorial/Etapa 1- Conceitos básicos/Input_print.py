# Exemplo de input de dados
nome = input("Digite seu nome: ")
print("Olá,", nome)

# Exemplo de print de dados
idade = 30
print("Sua idade é:", idade)

# Exemplo com múltiplos valores
nome = "João"
idade = 25
print("Nome:", nome, "Idade:", idade)

# Exemplo de formatação de texto em print
nome = "Ana"
idade = 28
print(f"Olá, {nome}! Você tem {idade} anos.")

# Outro exemplo de formatação usando o método format()
nome = "Pedro"
idade = 33
print("Olá, {}! Você tem {} anos.".format(nome, idade))
